package com.henyep.ib.terminal.server.helper.model;

public class IbTreeNode {

	private String id;
	private Integer min_id;
	private Integer max_id;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Integer getMin_id() {
		return min_id;
	}

	public void setMin_id(Integer min_id) {
		this.min_id = min_id;
	}

	public Integer getMax_id() {
		return max_id;
	}

	public void setMax_id(Integer max_id) {
		this.max_id = max_id;
	}

}
